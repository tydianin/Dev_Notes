// Arithmetic operators perform arithmetic on numbers.
let addition = '+';        // adds numbers
let subtraction = '-';     // subtracts numbers
let multiplication = '*';  // multiplies numbers
let exponentiation = '**'; // raises the first operand to the second operand power
// ^ logically equivalent to Math.pow(x,y);
let division = '/';        // returns the division quotient
let modulus = '%';         // returns the division remainder
let increment = '++';      // incremenets numbers
let decrement = '--';      // decrements numbers

// Numbers can be literals:
let x = 100 + 50;

// Numbers can be variables:
let a = 100;
let b = 50;
let c = a + b;

// And numbers can be expressions:
let n = (100 + 50) * a;

// Numbers are called 'Operands'.
// Operations are denoted by the 'Operator'.
// Operators follow PE(MD)(AS) precedence.